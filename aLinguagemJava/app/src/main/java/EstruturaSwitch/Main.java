package EstruturaSwitch;

public class Main {
    
    public static void main(String[] args) {
        
       int codigo = 2;
       
       switch(codigo){
           
           case 1:
               System.out.println("Vale 1!");
               break;
           case 2:
               System.out.println("Vale 2!");
               break;
           default:
               System.out.println("É isso ai!");
               break;
       }
        
    }
    
}
